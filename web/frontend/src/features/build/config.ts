import type { GenerateParams } from '@/api/client';

/**
 * The full trace-build configuration the node editor edits. It is a superset
 * of {@link GenerateParams}: every field except `out` maps 1:1 onto the
 * `/trace/generate` request body, while `out` is a CLI-only output directory
 * used solely by the YAML export (the live-run endpoint streams the trace back
 * in memory and ignores it).
 */
export interface BuildConfig {
  model: string;
  prompt: string;
  max_new_tokens: number;
  temperature: number;
  top_p: number;
  min_k: number;
  max_k: number;
  mass_threshold: number;
  capture_attention: boolean;
  capture_logit_lens: boolean;
  capture_activations: boolean;
  /** CLI output directory — YAML export only; not sent to the backend. */
  out: string;
}

