/** Project the editor config onto the `/trace/generate` request body. */
export function buildConfigToParams(config: BuildConfig): GenerateParams {
  return {
    model: config.model.trim(),
    prompt: config.prompt,
    max_new_tokens: config.max_new_tokens,
    temperature: config.temperature,
    top_p: config.top_p,
    min_k: config.min_k,
    max_k: config.max_k,
    mass_threshold: config.mass_threshold,
    capture_attention: config.capture_attention,
    capture_logit_lens: config.capture_logit_lens,
    capture_activations: config.capture_activations,
  };
}

/** True when the config is runnable / exportable (model + prompt both set). */
export function isBuildConfigValid(config: BuildConfig): boolean {
  return config.model.trim().length > 0 && config.prompt.trim().length > 0;
}

/**
 * Serialize the config to a `token-heatmap trace --config` YAML file, matching
 * the hand-written `configs/*.yaml` layout. Strings are emitted as JSON
 * double-quoted scalars, which are valid YAML and safely escape newlines and
 * quotes (e.g. multi-line prompts).
 */
export function buildConfigToYaml(config: BuildConfig): string {
  const q = (s: string) => JSON.stringify(s);
  const lines = [
    '# token-heatmap trace config — generated by the web node editor.',
    '# Run with: token-heatmap trace --config <this-file>.yaml',
    `model: ${q(config.model.trim())}`,
    `prompt: ${q(config.prompt)}`,
    '',
    `max_new_tokens: ${config.max_new_tokens}`,
    `temperature: ${config.temperature}`,
    `top_p: ${config.top_p}`,
    `min_k: ${config.min_k}`,
    `max_k: ${config.max_k}`,
    `mass_threshold: ${config.mass_threshold}`,
    '',
    `out: ${q(config.out.trim() || 'outputs/run')}`,
    '',
    `capture_attention: ${config.capture_attention}`,
    `capture_logit_lens: ${config.capture_logit_lens}`,
    `capture_activations: ${config.capture_activations}`,
    '',
  ];
  return lines.join('\n');
}
