export type Rgb = readonly [number, number, number];

const N = VIRIDIS_RGB.length;

/**
 * Sample the viridis colormap at `t ∈ [0, 1]`. Out-of-range values are clamped.
 * Linear interpolation between adjacent LUT entries; returns 8-bit RGB.
 */
export function sampleColor(t: number): Rgb {
  if (!Number.isFinite(t) || t <= 0) return VIRIDIS_RGB[0];
  if (t >= 1) return VIRIDIS_RGB[N - 1];
  const scaled = t * (N - 1);
  const i = Math.floor(scaled);
  const f = scaled - i;
  const a = VIRIDIS_RGB[i];
  const b = VIRIDIS_RGB[i + 1];
  return [
    Math.round(a[0] + (b[0] - a[0]) * f),
    Math.round(a[1] + (b[1] - a[1]) * f),
    Math.round(a[2] + (b[2] - a[2]) * f),
  ];
}

export function rgbToCss(rgb: Rgb): string {
  return `rgb(${rgb[0]}, ${rgb[1]}, ${rgb[2]})`;
}

export const VIRIDIS_STOPS = N;
